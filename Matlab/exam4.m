Ts=1/64;
T0=4;
N0=T0/Ts;
t=0:Ts:Ts*(N0-1);
g=exp(-2*t).*ustep(t);
%figure(1);plot(t,g);title('Orignal');
G1=Ts*0.5;
G2=Ts*g;
G=[G1 G2];
X=fft(G);
Y=ifft(X);
%figure(1);plot(t(1:256),Y(1:256));title('Orignal from inverse');
[Y1,Y2]=cart2pol(real(X),imag(X));
k=0:N0-1;
w=2*pi*(k/T0);
%figure(2);stem(w(1:32),Y1(1:32));
%figure(3);stem(w(1:32),Y2(1:32));
j=sqrt(-1);
g1=1./(2+(j*w));
[G3,G4]=cart2pol(real(g1),imag(g1));
%figure(4);stem(w(1:32),G4(1:32));hold on;plot(w(1:32),Y2(1:32));
